export function getCategory(slug: string) {
  return categories.find((c) => c.slug === slug);
}

export function getSubcategoriesForCategory(categorySlug: string) {
  return subcategories.filter((s) => s.categorySlug === categorySlug);
}

export function getSubcategory(categorySlug: string, subcategorySlug: string) {
  return subcategories.find((s) => s.categorySlug === categorySlug && s.slug === subcategorySlug);
}

export function getWorkflowsForSubcategory(categorySlug: string, subcategorySlug: string) {
  return workflows.filter((w) => w.categorySlug === categorySlug && w.subcategorySlug === subcategorySlug);
}

export function getWorkflow(id: string) {
  return workflows.find((w) => w.id.toLowerCase() === id.toLowerCase());
}
