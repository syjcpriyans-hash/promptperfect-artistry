const dressWorkflowIds = new Set(dressWorkflows.map((workflow) => workflow.id));

export const categories: Category[] = baseCategories;

export const subcategories: Subcategory[] = [
  ...baseSubcategories.filter(
    (subcategory) => !(subcategory.categorySlug === "clothes" && subcategory.slug === "dress"),
  ),
  dressSubcategory,
];

export const workflows: Workflow[] = [
  ...baseWorkflows.filter((workflow) => !dressWorkflowIds.has(workflow.id)),
  ...dressWorkflows,
];

export function getCategory(slug: string) {
  return categories.find((category) => category.slug === slug);
}

export function getSubcategoriesForCategory(categorySlug: string) {
  return subcategories.filter((subcategory) => subcategory.categorySlug === categorySlug);
}

export function getSubcategory(categorySlug: string, subcategorySlug: string) {
  return subcategories.find(
    (subcategory) => subcategory.categorySlug === categorySlug && subcategory.slug === subcategorySlug,
  );
}

export function getWorkflowsForSubcategory(categorySlug: string, subcategorySlug: string) {
  return workflows.filter(
    (workflow) => workflow.categorySlug === categorySlug && workflow.subcategorySlug === subcategorySlug,
  );
}

export function getWorkflow(id: string) {
  return workflows.find((workflow) => workflow.id.toLowerCase() === id.toLowerCase());
}
